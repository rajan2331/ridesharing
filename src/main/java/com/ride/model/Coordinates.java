package com.ride.model;

public class Coordinates {

	private Double xAxis;
	public Coordinates(Double xAxis, Double yAxis) {
		super();
		this.xAxis = xAxis;
		this.yAxis = yAxis;
	}

	private Double yAxis;

	public Double getxAxis() {
		return xAxis;
	}
	public Double getyAxis() {
		return yAxis;
	}

	

}
